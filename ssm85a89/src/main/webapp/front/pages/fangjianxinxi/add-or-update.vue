                                   fangjianleixing : false,
                                   tupian : false,
                                   jiage : false,
                                   zixundianhua : false,
                                   xiangxidizhi : false,
                                   fangjiansheshi : false,
                                   fangjianxiangqing : false,
                                   zhuangtai : false,
                                },
			}
		},
		components: {
			wPicker
		},
		computed: {
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
					},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
						// ss读取
																																																												
																		// 下拉框
			res = await this.$api.option(`fangjianleixing`,`fangjianleixing`,{});
			this.fangjianleixingOptions = res.data;
																																																// 自定义下拉框值
			this.zhuangtaiOptions = "未预约,已预约".split(',')
									
			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`fangjianxinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='fangjianhao'){
					this.ruleForm.fangjianhao = obj[o];
					this.ro.fangjianhao = true;
					continue;
					}
					if(o=='fangjianleixing'){
					this.ruleForm.fangjianleixing = obj[o];
					this.ro.fangjianleixing = true;
					continue;
					}
					if(o=='tupian'){
					this.ruleForm.tupian = obj[o];
					this.ro.tupian = true;
					continue;
					}
					if(o=='jiage'){
					this.ruleForm.jiage = obj[o];
					this.ro.jiage = true;
					continue;
					}
					if(o=='zixundianhua'){
					this.ruleForm.zixundianhua = obj[o];
					this.ro.zixundianhua = true;
					continue;
					}
					if(o=='xiangxidizhi'){
					this.ruleForm.xiangxidizhi = obj[o];
					this.ro.xiangxidizhi = true;
					continue;
					}
					if(o=='fangjiansheshi'){
					this.ruleForm.fangjiansheshi = obj[o];
					this.ro.fangjiansheshi = true;
					continue;
					}
					if(o=='fangjianxiangqing'){
					this.ruleForm.fangjianxiangqing = obj[o];
					this.ro.fangjianxiangqing = true;
					continue;
					}
					if(o=='zhuangtai'){
					this.ruleForm.zhuangtai = obj[o];
					this.ro.zhuangtai = true;
					continue;
					}
				}
			}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
																																																												
			// 多级联动参数
																																																												
																																																									
																																																									
															// 下拉变化
			fangjianleixingChange(e) {
				this.fangjianleixingIndex = e.target.value
				this.ruleForm.fangjianleixing = this.fangjianleixingOptions[this.fangjianleixingIndex]
			},
																																																// 下拉变化
			zhuangtaiChange(e) {
				this.zhuangtaiIndex = e.target.value
				this.ruleForm.zhuangtai = this.zhuangtaiOptions[this.zhuangtaiIndex]
			},
						
																											tupianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.tupian = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},
																																																															
			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
																																																																																																																																																																																																																																																																																																																																																																if((!this.ruleForm.fangjianhao)){
					this.$utils.msg(`房间号不能为空`);
					return
				}
																																												if((!this.ruleForm.fangjianleixing)){
					this.$utils.msg(`房间类型不能为空`);
					return
				}
																																																																																if((!this.ruleForm.jiage)){
					this.$utils.msg(`价格不能为空`);
					return
				}
												if(this.ruleForm.jiage&&(!this.$validate.isIntNumer(this.ruleForm.jiage))){
					this.$utils.msg(`价格应输入整数`);
					return
				}
																																																				if(this.ruleForm.zixundianhua&&(!this.$validate.isPhone(this.ruleForm.zixundianhua))){
					this.$utils.msg(`咨询电话应输入电话格式`);
					return
				}
																																																																																																																																																																												if(this.ruleForm.id){
					await this.$api.update(`fangjianxinxi`, this.ruleForm);
				}else{
					await this.$api.add(`fangjianxinxi`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>

